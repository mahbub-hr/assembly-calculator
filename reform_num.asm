REFORMNUM PROC

	PUSH	AX		; save registers
	PUSH	BX
	PUSH	CX
	PUSH	DX
	PUSH 	COUNT1
	PUSH 	COUNT2
	
	
	MOV 	CX,COUNT1
	CMP 	CX,COUNT2
	JG		@SEC
	JL 		@FIRST
	JMP 	@EXIT1
	
 @SEC:
	MOV 	COUNT,CX
	SUB 	CX,COUNT2
	MOV 	AX,10
	
	 @TOP1:	
		IMUL 	NUM2 	
		LOOP 	@TOP1
	
	MOV 	NUM2,AX
	JMP 	@EXIT1
	
 @FIRST:
	
	MOV 	AX,COUNT2
	MOV 	COUNT,AX
	SUB 	CX,COUNT2
	NEG 	CX
	MOV 	AX,10
	
	 @TOP2:	
		IMUL 	NUM1 		
		LOOP 	@TOP2
	
	MOV 	NUM1,AX
	
@EXIT1:
	
	POP 	COUNT2
	POP 	COUNT1
	POP 	DX
	POP 	CX
	POP 	BX
	POP 	AX
RET
REFORMNUM ENDP
	
	